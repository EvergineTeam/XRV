namespace Evergine.Xrv.Core.UI.Windows
{
    /// <summary>
    /// Behavior for more actions panel when one action button is pressed.
    /// </summary>
    public enum MoreActionsPanelBehavior
    {
        /// <summary>
        /// Automatically hides the panel once user taps on an action button.
        /// </summary>
        HideAutomatically,

        /// <summary>
        /// Panel stays open once user taps on an action button.
        /// </summary>
        StayOpen,
    }
}
