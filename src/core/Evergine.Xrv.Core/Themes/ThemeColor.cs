namespace Evergine.Xrv.Core.Themes
{
    /// <summary>
    /// Theme colors enum.
    /// </summary>
    public enum ThemeColor
    {
        /// <summary>
        /// Color used for:
        /// - Windows title bar plate.
        /// - Text color for buttons with light colors.
        /// - Lists: selected item background color.
        /// </summary>
        PrimaryColor1,

        /// <summary>
        /// Color used for:
        /// - Lists: scroll bar color.
        /// </summary>
        PrimaryColor2,

        /// <summary>
        /// Color used for:
        /// - Global text color.
        /// - Text color for active items in a tab control.
        /// </summary>
        PrimaryColor3,

        /// <summary>
        /// Color used for:
        /// - Text color for inactive items in a tab control.
        /// - Backplate color for some buttons.
        /// - Switch buttons: on state.
        /// - Lists: text color for selected item.
        /// - In general, to mark manipulation items as selected.
        /// </summary>
        SecondaryColor1,

        /// <summary>
        /// Color used for:
        /// - Manipulators color.
        /// </summary>
        SecondaryColor2,

        /// <summary>
        /// Color used for:
        /// - Switch buttons: off state.
        /// - Confirmation dialogs: accept option backplate.
        /// </summary>
        SecondaryColor3,

        /// <summary>
        /// Color used for:
        /// - Windows front plate gradient start.
        /// </summary>
        SecondaryColor4,

        /// <summary>
        /// Color used for:
        /// - Windows front plate gradient end.
        /// </summary>
        SecondaryColor5,
    }
}
